function AppInner() {
  const { state, send } = useScene();
  const { state: boardState, send: boardSend } = useBoard();
  const { state: graphState } = useGraph();
  const project = useProject();
  useGraphPersistence();
  const { saveNow } = useBoardPersistence();
  const dockviewApiRef = useRef<DockviewApi | null>(null);
  // Track which projectId we have already hydrated for. switchProject() now
  // re-runs hydration cleanly without leaking state from the previous project.
  const boardHydratedForRef = useRef<string | null>(null);
  const [cmdPaletteOpen, setCmdPaletteOpen] = useState(false);
  const [shortcutsOpen, setShortcutsOpen] = useState(false);

  // Hydrate board state on first render — and again after switchProject().
  //
  // Two paths:
  // 1. Normal: load from the user's project file (API-backed).
  // 2. /editor?learn=<key>: load a lesson's catalog board instead, so
  //    clicking "Open in IDE →" from /learn lands you in a playable copy
  //    of the circuit. The URL param is stripped after load so a refresh
  //    goes back to the user's saved project. NOTE: autosave has a 3-second
  //    hydration grace window, which gives users a moment to navigate away
  //    before the lesson board overwrites their saved project.
  useEffect(() => {
    if (boardHydratedForRef.current === project.projectId) return;
    boardHydratedForRef.current = project.projectId;

    const url = new URL(window.location.href);
    const learnKey = url.searchParams.get("learn");
    if (learnKey && boardCatalog[learnKey]) {
      boardSend({ type: "LOAD_BOARD", state: boardCatalog[learnKey] });
      // Strip the query param so refresh returns to the normal project flow.
      url.searchParams.delete("learn");
      window.history.replaceState(null, "", url.pathname + url.search + url.hash);
      return;
    }

    const pf = project.projectFile;
    if (pf.boardState) {
      boardSend({ type: "LOAD_BOARD", state: pf.boardState });
    }
  }, [project.projectId, project.projectFile, boardSend]);

  // Sync codegen to board whenever graph nodes/edges change.
  // Skip during the initial hydration — graph persistence replays saved nodes,
  // which would trigger codegen and overwrite the loaded sketchCode. The skip
  // guard is keyed by projectId so a switchProject() also gets a clean replay.
  const prevNodesRef = useRef(graphState.nodes);
  const prevEdgesRef = useRef(graphState.edges);
  const graphHydratedForRef = useRef<string | null>(null);
  useEffect(() => {
    // Reset prev refs and hydration flag whenever the project changes so the
    // next hydration replay isn't mistaken for a user edit.
    if (graphHydratedForRef.current !== project.projectId) {
      graphHydratedForRef.current = null;
      prevNodesRef.current = graphState.nodes;
      prevEdgesRef.current = graphState.edges;
    }

    if (
      graphState.nodes !== prevNodesRef.current ||
      graphState.edges !== prevEdgesRef.current
    ) {
      prevNodesRef.current = graphState.nodes;
      prevEdgesRef.current = graphState.edges;
      // Skip the first change after hydration (graph replay)
      if (graphHydratedForRef.current !== project.projectId) {
        graphHydratedForRef.current = project.projectId;
        return;
      }
      syncCodegenToBoard(graphState.nodes, graphState.edges, boardSend);
    }
  }, [graphState.nodes, graphState.edges, boardSend, project.projectId]);

  // Auto-generate sketch from board components when sketch is empty/auto-generated.
  // Skips regeneration when both components AND sketch changed in the same update
  // (e.g., agent/template placed components and wrote a sketch in one batch).
  const prevComponentsRef = useRef(boardState.components);
  const prevWiresRef = useRef(boardState.wires);
  const prevSketchRef = useRef(boardState.sketchCode);
  useEffect(() => {
    const componentsChanged =
      boardState.components !== prevComponentsRef.current ||
      boardState.wires !== prevWiresRef.current;
    const sketchChanged = boardState.sketchCode !== prevSketchRef.current;

    prevComponentsRef.current = boardState.components;
    prevWiresRef.current = boardState.wires;
    prevSketchRef.current = boardState.sketchCode;

    // If both changed in the same render, the sketch was intentionally set
    // (by agent/template) — don't overwrite it with auto-generated code.
    if (componentsChanged && sketchChanged) return;

    if (componentsChanged && isAutoGeneratedSketch(boardState.sketchCode)) {
      const generated = generateSketchFromBoard(boardState.components, boardState.wires);
      if (generated !== boardState.sketchCode) {
        boardSend({ type: "UPDATE_SKETCH", code: generated });
      }
    }
  }, [boardState.components, boardState.wires, boardState.sketchCode, boardSend]);

  useEffect(() => {
    function handleKeyDown(e: KeyboardEvent) {
      // Cmd/Ctrl+S — save project (works from anywhere, including editors)
      if ((e.metaKey || e.ctrlKey) && e.key === "s") {
        e.preventDefault();
        saveNow();
        return;
      }

      // Cmd/Ctrl+K — command palette
      if ((e.metaKey || e.ctrlKey) && e.key === "k") {
        e.preventDefault();
        setCmdPaletteOpen((prev) => !prev);
        return;
      }

      // ? — keyboard shortcuts help (only when not typing)
      if (e.key === "?" && !e.metaKey && !e.ctrlKey) {
        const tag = (e.target as HTMLElement).tagName;
        if (tag !== "INPUT" && tag !== "TEXTAREA" && !(e.target as HTMLElement).closest(".cm-editor")) {
          e.preventDefault();
          setShortcutsOpen((prev) => !prev);
          return;
        }
      }

      const target = e.target as HTMLElement
      const tag = target.tagName
      if (tag === "INPUT" || tag === "TEXTAREA") return;

      // Skip when focus is inside a CodeMirror editor — it has its own undo/redo
      if (target.closest(".cm-editor")) return;

      // Undo / Redo (both scene + board)
      if ((e.metaKey || e.ctrlKey) && e.key === "z") {
        e.preventDefault();
        send({ type: e.shiftKey ? "REDO" : "UNDO" });
        boardSend({ type: e.shiftKey ? "REDO" : "UNDO" });
        return;
      }

      if (e.key === "Delete" || e.key === "Backspace") {
        if (boardState.selectedId) {
          const isWire = boardState.selectedId in (boardState.wires ?? {});
          const name = isWire
            ? "Wire"
            : boardState.components[boardState.selectedId]?.name ?? "Component";
          if (isWire) {
            boardSend({ type: "REMOVE_WIRE", id: boardState.selectedId });
          } else {
            boardSend({ type: "REMOVE_COMPONENT", id: boardState.selectedId });
          }
          boardSend({ type: "SELECT", id: null });
          toast.info(`Deleted ${name} — press Cmd+Z to undo`);
        } else if (state.selectedId) {
          send({ type: "REMOVE", id: state.selectedId });
          toast.info("Deleted — press Cmd+Z to undo");
        }
      }
      if (e.key === "Escape") {
        send({ type: "SELECT", id: null });
        boardSend({ type: "SELECT", id: null });
      }
    }
    window.addEventListener("keydown", handleKeyDown);
    return () => window.removeEventListener("keydown", handleKeyDown);
  }, [state.selectedId, boardState.selectedId, send, boardSend, saveNow]);

  const LAYOUT_STORAGE_KEY = "dreamer:dockview-layout";

  const onReady = useCallback((event: DockviewReadyEvent) => {
    const api = event.api;
    dockviewApiRef.current = api;

    // Clear stale layouts from before Arduino simulator conversion.
    // The old layout references "canvas" and missing panels — force a fresh default.
    const LAYOUT_VERSION = "arduino-sim-v13";
    const saved = localStorage.getItem(LAYOUT_STORAGE_KEY);
    const savedVersion = localStorage.getItem(LAYOUT_STORAGE_KEY + ":version");
    if (saved && savedVersion === LAYOUT_VERSION) {
      try {
        const layout = JSON.parse(saved) as ReturnType<DockviewApi["toJSON"]>;
        api.fromJSON(layout);
        return setupPersistence(api);
      } catch {
        localStorage.removeItem(LAYOUT_STORAGE_KEY);
      }
    } else {
      // Remove stale layout
      localStorage.removeItem(LAYOUT_STORAGE_KEY);
    }

    // Default layout: Project | Breadboard | Sketch | Inspector
    //                                      | Serial  |        | Pin Inspector
    const totalWidth = api.width;

    const projectFilesPanel = api.addPanel({
      id: "projectFiles",
      component: "projectFiles",
      title: "Project",
    });

    const canvasPanel = api.addPanel({
      id: "breadboard",
      component: "breadboard",
      title: "Breadboard",
      position: { referencePanel: projectFilesPanel, direction: "right" },
    });

    // Sketch editor in the third column
    const sketchPanel = api.addPanel({
      id: "sketchEditor",
      component: "sketchEditor",
      title: "Sketch",
      position: { referencePanel: canvasPanel, direction: "right" },
    });

    api.addPanel({
      id: "schematic",
      component: "schematic",
      title: "Schematic",
      position: { referencePanel: sketchPanel, direction: "within" },
    });

    api.addPanel({
      id: "libraryManager",
      component: "libraryManager",
      title: "Libraries",
      position: { referencePanel: sketchPanel, direction: "within" },
    });

    api.addPanel({
      id: "diagram",
      component: "diagram",
      title: "Diagram",
      position: { referencePanel: sketchPanel, direction: "within" },
    });

    sketchPanel.api.setActive();

    // Inspector in the fourth column (right)
    const inspectorPanel = api.addPanel({
      id: "inspector",
      component: "inspector",
      title: "Inspector",
      position: { referencePanel: sketchPanel, direction: "right" },
    });

    // Pin Inspector below Inspector
    api.addPanel({
      id: "pinInspector",
      component: "pinInspector",
      title: "Pin Inspector",
      position: { referencePanel: inspectorPanel, direction: "below" },
    });

    // Serial Monitor below breadboard (aligns with code output in sketch)
    const serialPanel = api.addPanel({
      id: "serialMonitor",
      component: "serialMonitor",
      title: "Serial Monitor",
      position: { referencePanel: canvasPanel, direction: "below" },
    });

    projectFilesPanel.api.setSize({ width: totalWidth * 0.14 });
    canvasPanel.api.setSize({ width: totalWidth * 0.45 });
    sketchPanel.api.setSize({ width: totalWidth * 0.18 });
    inspectorPanel.api.setSize({ width: totalWidth * 0.20 });

    const totalHeight = api.height;
    serialPanel.api.setSize({ height: totalHeight * 0.30 });

    setupPersistence(api);
  }, []);

  const debounceRef = useRef<ReturnType<typeof setTimeout>>(undefined);

  function setupPersistence(api: DockviewApi) {
    api.onDidLayoutChange(() => {
      clearTimeout(debounceRef.current);
      debounceRef.current = setTimeout(() => {
        const layout = api.toJSON();
        localStorage.setItem(LAYOUT_STORAGE_KEY, JSON.stringify(layout));
        localStorage.setItem(LAYOUT_STORAGE_KEY + ":version", "arduino-sim-v12");
      }, 300);
    });
  }

  return (
    <DockviewContext.Provider value={dockviewApiRef}>
      {/* BottomToolbar sits in its own row at the bottom (not absolute) so
          it reserves height and never overlays/blocks the dockview content
          above. The toolbar itself owns its background + top border so it
          reads as a chrome strip rather than a floating popover.
          AI history, when open, lives inside the toolbar row and grows
          upward — it still overflows on top of the dockview when needed,
          but the always-visible bar no longer obscures content. */}
      <div className="flex flex-col w-full h-full">
        <div className="flex-1 min-h-0 dockview-theme-abyss">
          <DockviewReact
            onReady={onReady}
            components={components}
            className="h-full"
          />
        </div>
        <BottomToolbar />
      </div>
      <CommandPalette open={cmdPaletteOpen} onClose={() => setCmdPaletteOpen(false)} />
      <ShortcutsDialog open={shortcutsOpen} onClose={() => setShortcutsOpen(false)} />
    </DockviewContext.Provider>
  );
}

function AppOrDocs() {
  const { path, navigate } = useRouter();

  // Redirect root to the editor.
  useEffect(() => {
    if (path === "/") navigate("/editor");
  }, [path, navigate]);

  if (path.startsWith("/documentation")) {
    return <DocsRouter />;
  }
  if (path.startsWith("/learn")) {
    return <LearnRouter />;
  }
  // /editor (and /, briefly, until the redirect fires) render the main app.
  return (
    <ProjectLoader>
      <AppProviders>
        <AppInner />
      </AppProviders>
    </ProjectLoader>
  );
}

export default function App() {
  return (
    <ErrorBoundary name="App">
      <Router>
        <AppOrDocs />
      </Router>
      <ToastContainer />
    </ErrorBoundary>
  );
}
